package com.example.ero.week2homework1;

public class Menyu {

    private String imageUrl;
    private String title;
    private String text;
    private String kaloria;
    private String menyuUrl;

    public String getMenyuUrl() {
        return menyuUrl;
    }

    public void setMenyuUrl(String menyuUrl) {
        this.menyuUrl = menyuUrl;
    }

    public String getKaloria() {
        return kaloria;
    }

    public void setKaloria(String kaloria) {
        this.kaloria = kaloria;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
